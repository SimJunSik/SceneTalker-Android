package com.android.yapp.scenetalker;

public class CountInfo {
    String episode;
    int soda_count;
    int sweet_potato_count;

    public String getEpisode() {
        return episode;
    }

    public void setEpisode(String episode) {
        this.episode = episode;
    }

    public int getSoda_count() {
        return soda_count;
    }

    public void setSoda_count(int soda_count) {
        this.soda_count = soda_count;
    }

    public int getSweet_potato_count() {
        return sweet_potato_count;
    }

    public void setSweet_potato_count(int sweet_potato_count) {
        this.sweet_potato_count = sweet_potato_count;
    }

}
